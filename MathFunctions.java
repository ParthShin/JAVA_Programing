public class MathFunctions {
    public static void main(String[] args) {
        System.out.println("For abs Function :" + Math.abs(-4.5));
        System.out.println("For aqrt Function :" + Math.sqrt(9));
        System.out.println("For min Function :" + Math.min(5, 10));  
        System.out.println("For max Function :" + Math.max(5, 10));
    }
}
